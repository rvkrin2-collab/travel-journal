const daysRoot = document.querySelector('#days');

function imageUrl(publicId, width = 1800) {
  return `https://res.cloudinary.com/${CLOUD_NAME}/image/upload/f_auto,q_auto,w_${width}/${publicId}.jpg`;
}

function renderStoryItem(item, index) {
  return `
    <article class="photo-story ${index === 0 ? 'photo-story--hero' : ''}">
      <img src="${imageUrl(item.publicId)}" alt="${item.title}" loading="${index === 0 ? 'eager' : 'lazy'}">
      <div class="photo-caption">
        <h3>${item.title}</h3>
        <p>${item.text}</p>
      </div>
    </article>
  `;
}

function renderBackstageItem(item) {
  return `
    <article class="backstage-card">
      <img src="${imageUrl(item.publicId, 900)}" alt="${item.title}" loading="lazy">
      <p>${item.title}</p>
    </article>
  `;
}

function renderItinerary() {
  return `
    <section class="itinerary">
      <p class="eyebrow">Маршрут по дням</p>
      <h2>Вся поездка</h2>
      <div class="days-list">
        ${days.map(day => `
          <article class="day-card">
            <div>
              <div class="day-num">${String(day.n).padStart(2, '0')}</div>
              <div class="day-date">${day.date}</div>
            </div>
            <div>
              <h3>${day.title}</h3>
              <p>${day.text}</p>
              <div class="tags">${day.tags.map(tag => `<span>${tag}</span>`).join('')}</div>
            </div>
          </article>
        `).join('')}
      </div>
    </section>
  `;
}

function renderBackstage() {
  return `
    <section class="backstage">
      <div class="story-head">
        <p class="eyebrow">За кадром</p>
        <h2>Хорошие, но не основные кадры</h2>
        <p>Эти фотографии не ведут рассказ, но сохраняют атмосферу дня и могут пригодиться для расширенной галереи.</p>
      </div>
      <div class="backstage-grid">
        ${backstage.map(renderBackstageItem).join('')}
      </div>
    </section>
  `;
}

