const daysRoot = document.querySelector('#days');

function imageUrl(publicId) {
  return `https://res.cloudinary.com/${CLOUD_NAME}/image/upload/f_auto,q_auto,w_1800/${publicId}.jpg`;
}

function renderStoryItem(item, index) {
  return `
    <article class="photo-story ${index === 0 ? 'photo-story--hero' : ''}">
      <img src="${imageUrl(item.publicId)}" alt="${item.title}" loading="${index === 0 ? 'eager' : 'lazy'}">
      <div class="photo-caption">
        <h3>${item.title}</h3>
        <p>${item.text}</p>
      </div>
    </article>
  `;
}

function renderItinerary() {
  return `
    <section class="itinerary">
      <p class="eyebrow">Маршрут по дням</p>
      <h2>Вся поездка</h2>
      <div class="days-list">
        ${days.map(day => `
          <article class="day-card">
            <div>
              <div class="day-num">${String(day.n).padStart(2, '0')}</div>
              <div class="day-date">${day.date}</div>
            </div>
            <div>
              <h3>${day.title}</h3>
              <p>${day.text}</p>
              <div class="tags">${day.tags.map(tag => `<span>${tag}</span>`).join('')}</div>
            </div>
          </article>
        `).join('')}
      </div>
    </section>
  `;
}

function render() {
  const storyHtml = `
    <section class="story" id="story-day-1">
      <div class="story-head">
        <p class="eyebrow">День 1 · 4 июля 2026</p>
        <h2>Из Бишкека к башне Бурана и дальше в Чон-Кемин</h2>
        <p>Первый день начался как обычный выезд из города, а закончился уже среди гор. Главная остановка — башня Бурана: остаток древнего Баласагуна, города Шёлкового пути, от которого сегодня остались минарет, земляные валы и каменные изваяния.</p>
      </div>
      <div class="story-photos">
        ${story.map(renderStoryItem).join('')}
      </div>
    </section>
  `;

  daysRoot.innerHTML = storyHtml + renderItinerary();
}

render();
